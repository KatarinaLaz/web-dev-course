import express from "express";
import bodyParser from "body-parser";

const app = express();
const port = 3000;

app.use(bodyParser.urlencoded({ extended: true }));
app.use(express.static("public"));  // We put all of our static files (files that don't change) in the public folder and so that Express knows and treat them as such
                                    // If the file is an src folder, for example, we write like this (express.static("src/public"));
                                    // When we reference these files, the public folder becomes the starting point. Example:
                                    // <link rel="stylesheet" href="/styles/layout.css"> => the styles folder is inside the public folder

